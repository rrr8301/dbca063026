#!/usr/bin/env bash

set -e

export MICROPY_MAINTAINER_BUILD=1

# Set known OPEN_MAX (NO_FILES) limit
ulimit -n 1024

# Ensure we have nproc for parallel builds
if which nproc > /dev/null; then
    MAKEOPTS="-j$(nproc)"
else
    MAKEOPTS="-j1"
fi

echo "=== MicroPython coverage_32bit job ==="

# Step 1: Install packages (32-bit setup)
echo "Step 1: Installing packages..."
python3 -m pip install pyelftools ar
gcc --version
python3 --version

# Step 2: Build (coverage_32bit_build)
echo "Step 2: Building mpy-cross..."
make ${MAKEOPTS} -C mpy-cross

echo "Step 2b: Building unix port with coverage and 32-bit support..."
make ${MAKEOPTS} -C ports/unix VARIANT=coverage MICROPY_FORCE_32BIT=1 submodules
make ${MAKEOPTS} -C ports/unix VARIANT=coverage MICROPY_FORCE_32BIT=1 deplibs
make ${MAKEOPTS} -C ports/unix VARIANT=coverage MICROPY_FORCE_32BIT=1

echo "Step 2c: Building ffi_lib.so for 32-bit..."
gcc -m32 -shared -o tests/ports/unix/ffi_lib.so tests/ports/unix/ffi_lib.c

# Step 3: Run main test suite (coverage_32bit_run_tests)
echo "Step 3: Running main test suite..."
make -C ports/unix VARIANT=coverage MICROPY_FORCE_32BIT=1 test_full || true

echo "Step 3b: Running extra tests..."
micropython=./ports/unix/build-coverage/micropython
(cd tests && MICROPY_CPYTHON3=python3 MICROPY_MICROPYTHON=$micropython ./run-multitests.py multi_net/*.py || true)
(cd tests && MICROPY_CPYTHON3=python3 MICROPY_MICROPYTHON=$micropython ./run-perfbench.py --average 1 1000 1000 || true)

# Step 4: Build native mpy modules (native_mpy_modules_32bit_build)
echo "Step 4: Building native mpy modules for x86..."
for natmod in btree deflate features1 features3 features4 framebuf heapq random re
do
    make -C examples/natmod/$natmod ARCH=x86 clean
    make -C examples/natmod/$natmod ARCH=x86
done

# features2 requires soft-float on certain architectures
make -C examples/natmod/features2 ARCH=x86 clean
make -C examples/natmod/features2 ARCH=x86

# Step 5: Test importing .mpy generated by mpy_ld.py (unix_coverage_32bit_run_native_mpy_tests)
echo "Step 5: Testing native mpy imports..."
MICROPYPATH=examples/natmod/features2 ./ports/unix/build-coverage/micropython -m features2
(cd tests && ./run-natmodtests.py --arch x86 extmod/*.py || true)

# Step 6: Print failures
echo "Step 6: Printing test failures (if any)..."
tests/run-tests.py --print-failures || true

echo "=== Build and tests completed ==="
echo "FINAL_STATUS = SUCCESS"
