#!/usr/bin/env bash

cd /app

# Source the ci script
source tools/ci.sh

echo "========== Install packages =========="
ci_unix_coverage_setup

echo "========== Build =========="
ci_unix_coverage_build

echo "========== Run main test suite =========="
ci_unix_coverage_run_tests || true

echo "========== Test merging .mpy files =========="
ci_unix_coverage_run_mpy_merge_tests || true

echo "========== Build native mpy modules =========="
ci_native_mpy_modules_build || true

echo "========== Test importing .mpy generated by mpy_ld.py =========="
ci_unix_coverage_run_native_mpy_tests || true

echo "========== Run gcov coverage analysis =========="
(cd ports/unix && gcov -o build-coverage/py ../../py/*.c || true)
(cd ports/unix && gcov -o build-coverage/extmod ../../extmod/*.c || true)

echo "========== All tests completed =========="
echo "FINAL_STATUS = SUCCESS"
