#!/bin/bash

set -e

# Clone repository if not already present
if [ ! -d "/workspace/micropython" ]; then
    echo "Cloning MicroPython repository..."
    git clone https://github.com/micropython/micropython.git /workspace/micropython
fi

cd /workspace/micropython

# Initialize submodules
echo "Initializing submodules..."
git submodule update --init --recursive

# Install Python dependencies
echo "Installing Python dependencies..."
python3 -m pip install -q -r docs/requirements.txt 2>/dev/null || true

# Run coverage setup
echo "Running coverage setup..."
bash tools/ci.sh unix_coverage_setup

# Build MicroPython with coverage
echo "Building MicroPython with coverage..."
bash tools/ci.sh unix_coverage_build

# Run main test suite
echo "Running main test suite..."
bash tools/ci.sh unix_coverage_run_tests || TEST_SUITE_FAILED=1

# Test merging .mpy files
echo "Testing .mpy file merging..."
bash tools/ci.sh unix_coverage_run_mpy_merge_tests || MPY_MERGE_FAILED=1

# Build native mpy modules
echo "Building native mpy modules..."
bash tools/ci.sh native_mpy_modules_build || NATIVE_MPY_BUILD_FAILED=1

# Test importing .mpy generated by mpy_ld.py
echo "Testing native mpy imports..."
bash tools/ci.sh unix_coverage_run_native_mpy_tests || NATIVE_MPY_TEST_FAILED=1

# Run gcov coverage analysis
echo "Running gcov coverage analysis..."
(cd ports/unix && gcov -o build-coverage/py ../../py/*.c || true)
(cd ports/unix && gcov -o build-coverage/extmod ../../extmod/*.c || true)

# Print test failures if any occurred
if [ -n "$TEST_SUITE_FAILED" ] || [ -n "$MPY_MERGE_FAILED" ] || [ -n "$NATIVE_MPY_BUILD_FAILED" ] || [ -n "$NATIVE_MPY_TEST_FAILED" ]; then
    echo "Printing test failures..."
    python3 tests/run-tests.py --print-failures || true
fi

# Exit with failure if any test phase failed
if [ -n "$TEST_SUITE_FAILED" ] || [ -n "$MPY_MERGE_FAILED" ] || [ -n "$NATIVE_MPY_BUILD_FAILED" ] || [ -n "$NATIVE_MPY_TEST_FAILED" ]; then
    exit 1
fi

echo "All tests completed successfully!"
exit 0