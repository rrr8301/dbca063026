#!/bin/bash

set -e  # Exit immediately if a command exits with a non-zero status

# Activate Python environment
source /app/venv/bin/activate

# Install project dependencies
# Modify tools/ci.sh to avoid using sudo
sed -i 's/sudo //g' tools/ci.sh
# Ensure non-interactive installation
sed -i 's/apt-get install/apt-get install -y/g' tools/ci.sh
tools/ci.sh unix_32bit_setup

# Build the project
tools/ci.sh unix_coverage_32bit_build

# Verify the build output
if [ ! -f "./ports/unix/build-coverage/micropython" ]; then
    echo "Error: micropython executable not found!"
    exit 1
fi

# Run the main test suite
tools/ci.sh unix_coverage_32bit_run_tests

# Build native mpy modules
tools/ci.sh native_mpy_modules_32bit_build

# Test importing .mpy generated by mpy_ld.py
tools/ci.sh unix_coverage_32bit_run_native_mpy_tests

# Print failures if any
tests/run-tests.py --print-failures